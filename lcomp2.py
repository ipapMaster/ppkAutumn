# Задача (list comprehension)
"""
1. строка s = ''
2. множества s = set()
3. списки s = []
4. кортежи s = ()
5. словари - предстоит
"""
s = 'лето осень зима весна'

# Внести их в виде строки в двойных кавычках через запятую
# ["лето", "осень", "зима", "весна"]

temp = [f'"{i}"' for i in s.split()]

lst = ', '.join(temp)

print(f'[{lst}]')
