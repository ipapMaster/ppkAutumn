# Функция diet()

food = {
    'жирное': ['бургер', 'пицца', 'шашлык'],
    'сладкое': ['торт', 'мороженое', 'конфеты'],
    'мучное': ['булочки', 'печенье', 'хлеб'],
    'диетическое': ['овощи', 'фрукты', 'гречка', 'курица']
}

def diet(meal_list):
    # Разделяем строку на список блюд
    meals = meal_list.split(', ')

    # Счетчик для диетических блюд
    diet_count = 0

    # Анализ каждого блюда
    for meal in meals:
        if meal in food['диетическое']:
            diet_count += 1

    # Проверка условия
    if diet_count <= len(meals) / 2:
        return 'Не ешь это!'

    return 'На здоровье!'


# Примеры
print(diet('гречка, овощи, курица, фрукты, кефир'))  # на здоровье
print(diet('бургер, пицца, торт, мороженое, булочки'))  # не ешь это
print(diet('овощи, шашлык, фрукты, конфеты, гречка'))  # на здоровье
print(diet('курица, овощи, фрукты, гречка, мороженое'))  # на здоровье
print(diet('овощи, фрукты, курица, гречка, творог'))  # на здоровье
print(diet('булочки, печенье, хлеб, торт, конфеты'))  # не ешь это
print(diet('шашлык, овощи, пицца, фрукты, гречка'))  # на здоровье
